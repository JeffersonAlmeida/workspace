package com.motorola.btc.research.target.common.ucdoc;

/**
 * This class represents a document revision. It contains the revision number and date, the author of the 
 * revision and the revision description containing the some comments about the changes that were performed.  
 * @author jrm687
 *
 */
public class Revision {
	//INSPECT New class created by jrm687
	private String revisionNumber;
	
	private String revisionDate;
	
	private String author;
	
	private String description;
	
	/**
	 * Class constructor.
	 * @param revisionNumber the number of the revision
	 * @param revisionDate the date of the revision
	 * @param author the revision author
	 * @param description some comments about the revision
	 */
	public Revision(String revisionNumber, String revisionDate, String author,
			String description) {
		super();
		this.revisionNumber = revisionNumber;
		this.revisionDate = revisionDate;
		this.author = author;
		this.description = description;
	}
	
	/**
	 * Returns a textual representation of the <code>Revision</code> object.
	 */
	public String toString(){
		return this.revisionNumber +" "+ this.revisionDate +" "+ this.author +" "+ this.description;
	}
	
	/**
     * Get method for <code>revisionNumber</code> attribute.
     * 
     * @return Returns the <code>revisionNumber</code>.
     */
	public String getRevisionNumber() {
		return revisionNumber;
	}
	
	/**
     * Sets the <code>revisionNumber</code> attribute.
     * 
     * @param revisionHistory The new <code>revisionNumber</code> value.
     */
	public void setRevisionNumber(String revisionNumber) {
		this.revisionNumber = revisionNumber;
	}
	
	/**
     * Get method for <code>revisionDate</code> attribute.
     * 
     * @return Returns the <code>revisionDate</code>.
     */
	public String getRevisionDate() {
		return revisionDate;
	}
	
	/**
     * Sets the <code>revisionDate</code> attribute.
     * 
     * @param revisionHistory The new <code>revisionDate</code> value.
     */
	public void setRevisionDate(String revisionDate) {
		this.revisionDate = revisionDate;
	}
	
	/**
     * Get method for <code>author</code> attribute.
     * 
     * @return Returns the <code>author</code>.
     */
	public String getAuthor() {
		return author;
	}
	
	/**
     * Sets the <code>author</code> attribute.
     * 
     * @param revisionHistory The new <code>author</code> value.
     */
	public void setAuthor(String author) {
		this.author = author;
	}
	
	/**
     * Get method for <code>description</code> attribute.
     * 
     * @return Returns the <code>description</code>.
     */
	public String getDescription() {
		return description;
	}
	
	/**
     * Sets the <code>description</code> attribute.
     * 
     * @param revisionHistory The new <code>description</code> value.
     */
	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
	

}
