package com.motorola.btc.research.target.common.exceptions;

/**
 * 
 * Represents a general TaRGeT exception
 *<pre>
 * CLASS:
 * This class must be used as superclass of all exception thrown by TaRGeT 
 * 
 * RESPONSIBILITIES:
 * Standardize the TaRGeT exceptions
 *
 * COLABORATORS:
 * All exceptions in TaRGeT code must extend this class.
 *
 * USAGE:
 * 
 */
public class TargetException extends Exception
{
    /** Default Serial ID. */
    private static final long serialVersionUID = 1L;
    
    /**
     * 
     * Constructor based on superclass.
     */
    public TargetException()
    {
        super();
    }
    
    /**
     * 
     * Constructor based on superclass.
     * 
     * @param message The error message. 
     */
    public TargetException(String message)
    {
        super(message);
    }
}
