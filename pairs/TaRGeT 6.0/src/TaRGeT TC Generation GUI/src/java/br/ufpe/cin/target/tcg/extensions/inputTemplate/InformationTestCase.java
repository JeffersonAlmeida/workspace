package br.ufpe.cin.target.tcg.extensions.inputTemplate;

/**
 * This class contains the information imported from the template file.
 * 
 * <pre>
 * CLASS:
 * This class stores the information imported from the template.
 * 
 * RESPONSIBILITIES:
 * This class keeps the information imported from the templates (e.g.: header)
 * 
 * USAGE:
 * InformationTestCase infoTestCase = new InformationTestCase();
 * infoTestCase.setSuiteName(String);
 * infoTestCase.getSuiteName(String);
 */
public class InformationTestCase 
{
	private String suiteName;

	private String dateExported;

	private String description;

	private String createdBy;

	private String lastModifiedBy;

	private String suiteStatus;

	private String ownershipGroup;

	private String readPermissions;

	private String writePermissions;

	private String attachedFileDesc;

	private String procedure;

	private String notes;
	
	private String status;
	
	private String regressionLevel;
	
	private String executionMethod;

    private String fileName;

	public InformationTestCase() 
	{
	}
	
	public InformationTestCase(String suiteName, String dateExported, String description, String createdBy,
			String lastModifiedBy, String suiteStatus, String ownershipGroup, String readPermissions,
			String writePermissions, String attachedFileDesc, String procedure, String notes, String status,
			String regressionLevel, String executionMethod, String fileName) 
	{
		this.suiteName = suiteName;
		this.dateExported = dateExported;
		this.description = description;
		this.createdBy = createdBy;
		this.lastModifiedBy = lastModifiedBy;
		this.suiteStatus = suiteStatus;
		this.ownershipGroup = ownershipGroup;
		this.readPermissions = readPermissions;
		this.writePermissions = writePermissions;
		this.attachedFileDesc = attachedFileDesc;
		this.procedure = procedure;
		this.notes = notes;
		this.status = status;
		this.regressionLevel = regressionLevel;
		this.executionMethod = executionMethod;
		this.fileName = fileName;
	}

	public String getSuiteName() 
	{
		return suiteName;
	}

	public void setSuiteName(String suiteName) 
	{
		this.suiteName = suiteName;
	}

	public String getDateExported() 
	{
		return dateExported;
	}

	public void setDateExported(String dateExported) 
	{
		this.dateExported = dateExported;
	}

	public String getDescription() 
	{
		return description;
	}

	public void setDescription(String description) 
	{
		this.description = description;
	}

	public String getCreatedBy() 
	{
		return createdBy;
	}

	public void setCreatedBy(String createdBy) 
	{
		this.createdBy = createdBy;
	}

	public String getLastModifiedBy() 
	{
		return lastModifiedBy;
	}

	public void setLastModifiedBy(String lastModifiedBy) 
	{
		this.lastModifiedBy = lastModifiedBy;
	}

	public String getSuiteStatus() 
	{
		return suiteStatus;
	}

	public void setSuiteStatus(String suiteStatus) 
	{
		this.suiteStatus = suiteStatus;
	}

	public String getOwnershipGroup() 
	{
		return ownershipGroup;
	}

	public void setOwnershipGroup(String ownershipGroup) 
	{
		this.ownershipGroup = ownershipGroup;
	}

	public String getReadPermissions() 
	{
		return readPermissions;
	}

	public void setReadPermissions(String readPermissions) 
	{
		this.readPermissions = readPermissions;
	}

	public String getWritePermissions() 
	{
		return writePermissions;
	}

	public void setWritePermissions(String writePermissions) 
	{
		this.writePermissions = writePermissions;
	}

	public String getAttachedFileDesc() 
	{
		return attachedFileDesc;
	}

	public void setAttachedFileDesc(String attachedFileDesc) 
	{
		this.attachedFileDesc = attachedFileDesc;
	}

	public String getProcedure() 
	{
		return procedure;
	}

	public void setProcedure(String procedure) 
	{
		this.procedure = procedure;
	}

	public String getNotes() 
	{
		return notes;
	}

	public void setNotes(String notes) 
	{
		this.notes = notes;
	}
	
	public void setStatus(String status) {
		this.status = status;
	}
	
	public String getStatus() {
		return status;
	}
	
	public String getRegressionLevel() {
		return regressionLevel;
	}

	public void setRegressionLevel(String regressionLevel) {
		this.regressionLevel = regressionLevel;
	}

	public String getExecutionType() {
		return executionMethod;
	}

	public void setExecutionMethod(String executionMethod) {
		this.executionMethod = executionMethod;
	}

    /**
     * Sets the file name.
     * @param name
     */
    public void setFileName(String name)
    {
        this.fileName = name;
    }

    /**
     * Gets the fileName value.
     *
     * @return Returns the fileName.
     */
    public String getFileName()
    {
        return fileName;
    }
}
