package com.motorola.btc.research.cnlframework.vocabulary.terms;

import com.motorola.btc.research.cnlframework.vocabulary.PartsOfSpeech;

/**
 * This class represents a lexical entry of the type Adjective Term.
 * @author
 *
 */
public class AdjectiveTerm extends LexicalEntry {
	
	/**
	 * Class constructor.
	 * Creates an adjective term with the term value passed as parameter. 
	 * @param term the term value
	 */
	public AdjectiveTerm(String term) {
		super(PartsOfSpeech.JJ, term);
	}
	
	/**
	 * 
	 * @return the adjective term value
	 */
	public String getTerm()
	{
		return this.getTerm(PartsOfSpeech.JJ);
	}

    
	/**
	 * 
	 * @return the adjective string value
	 */
    public String toString()
    {
        return "Adjective: " + super.toString();
    }
	
	
}
